$("#dna1").on('input', function () {
    if (this.value == "A") {
        $("#rna1").val("U")
    } else if (this.value == "C") {
        $("#rna1").val("G")
    } else if (this.value == "G") {
        $("#rna1").val("C")
    } else if (this.value == "T") {
        $("#rna1").val("A")
    }
})

